    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        // TODO add your handling code here:

        String pathAbs = path;
        //bitplane
        if (TabbedPanel.getSelectedIndex() == 0) {
            this.updateGambar(lblBit1, imaging.BitPlane(1));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit2, imaging.BitPlane(2));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit3, imaging.BitPlane(3));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit4, imaging.BitPlane(4));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit5, imaging.BitPlane(5));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit6, imaging.BitPlane(6));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit7, imaging.BitPlane(7));
            imaging.setFile(pathAbs);
            this.updateGambar(lblBit8, imaging.BitPlane(8));
        } else if (TabbedPanel.getSelectedIndex() == 1) {
            imaging.setFile(pathAbs);
            this.updateGambar(lblNegative, imaging.ImageNegative());
        } else if (TabbedPanel.getSelectedIndex() == 2) {
            imaging.setFile(pathAbs);
            this.updateGambar(lblLog, imaging.LogTransformation(Integer.parseInt(txtC.getText())));
        } else if (TabbedPanel.getSelectedIndex() == 3) {
            imaging.setFile(pathAbs);
            this.updateGambar(lblPower, imaging.PowerLawTransformation(Integer.parseInt(txtC.getText()), Integer.parseInt(txtY.getText())));
        }

    }//GEN-LAST:event_btnProsesActionPerformed

